"""Build a presence-only Construction-PPE dataset for YOLO training.

The upstream dataset mixes PPE objects, person boxes, "none", and weak "no_*"
violation classes. Runtime violation detection is more reliable when the model
learns strong positive classes and the app infers missing PPE per person.
"""

from __future__ import annotations

import json
import os
import shutil
from collections import Counter
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
SOURCE = ROOT / "data" / "ppe_construction"
TARGET = ROOT / "data" / "ppe_construction_presence"
CONFIG = ROOT / "backend" / "configs" / "ppe_presence_yolo11.yaml"

CLASS_REMAP = {
    0: 0,  # helmet
    1: 1,  # gloves
    2: 2,  # vest
    3: 3,  # boots
    4: 4,  # goggles
    6: 5,  # Person
}

NAMES = {
    0: "helmet",
    1: "gloves",
    2: "vest",
    3: "boots",
    4: "goggles",
    5: "Person",
}

IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


def link_or_copy(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists():
        return
    try:
        os.link(src, dst)
    except OSError:
        shutil.copy2(src, dst)


def convert_label(src: Path, dst: Path) -> Counter[int]:
    dst.parent.mkdir(parents=True, exist_ok=True)
    counts: Counter[int] = Counter()
    output_lines: list[str] = []

    if src.exists():
        for line in src.read_text(encoding="utf-8", errors="ignore").splitlines():
            parts = line.strip().split()
            if len(parts) < 5:
                continue
            old_class = int(float(parts[0]))
            if old_class not in CLASS_REMAP:
                continue
            new_class = CLASS_REMAP[old_class]
            output_lines.append(" ".join([str(new_class), *parts[1:5]]))
            counts[new_class] += 1

    dst.write_text("\n".join(output_lines) + ("\n" if output_lines else ""), encoding="utf-8")
    return counts


def write_config() -> None:
    CONFIG.parent.mkdir(parents=True, exist_ok=True)
    names_yaml = "\n".join(f"  {idx}: {name}" for idx, name in NAMES.items())
    CONFIG.write_text(
        "\n".join(
            [
                "# Presence-only Construction-PPE dataset.",
                "# Generated by scripts/prepare_ppe_presence_dataset.py.",
                "",
                "path: data/ppe_construction_presence",
                "train: images/train",
                "val: images/val",
                "test: images/test",
                "",
                "names:",
                names_yaml,
                "",
            ]
        ),
        encoding="utf-8",
    )


def main() -> None:
    if not SOURCE.exists():
        raise FileNotFoundError(f"Source dataset not found: {SOURCE}")

    summary: dict[str, dict[str, int | dict[str, int]]] = {}

    for split in ("train", "val", "test"):
        split_counts: Counter[int] = Counter()
        images = [
            p
            for p in (SOURCE / "images" / split).iterdir()
            if p.is_file() and p.suffix.lower() in IMAGE_EXTENSIONS
        ]

        for image in images:
            rel_image = image.relative_to(SOURCE)
            target_image = TARGET / rel_image
            link_or_copy(image, target_image)

            source_label = SOURCE / "labels" / split / f"{image.stem}.txt"
            target_label = TARGET / "labels" / split / f"{image.stem}.txt"
            split_counts.update(convert_label(source_label, target_label))

        summary[split] = {
            "images": len(images),
            "objects": sum(split_counts.values()),
            "classes": {NAMES[idx]: split_counts[idx] for idx in sorted(NAMES)},
        }

    write_config()
    (TARGET / "summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")

    print(f"Presence dataset ready: {TARGET}")
    print(f"YOLO config ready: {CONFIG}")
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
